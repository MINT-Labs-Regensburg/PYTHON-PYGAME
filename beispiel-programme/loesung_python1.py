# Lösung Aufgabe 1: Variablen

# 1. Erstelle Variablen für name, alter und groesse
name = "Nicole"
alter = 12
groesse = 1.57

# 2. Gib sie mit print() aus
print("Ich heisse", name, "bin", alter, "Jahre alt und", groesse, "Meter gross")

# 3. Verändere das alter um +1
alter += 1
print("Nach einem Jahr bin ich", alter, "Jahre alt")

# 4. Berechne summe von zwei Zahlen
zahl1 = 10
zahl2 = 25
summe = zahl1 + zahl2
print("Die Summe von", zahl1, "und", zahl2, "ist", summe)

# 5. Frage den Nutzer nach seiner Lieblingsfarbe mit input()
